# ---------------------------------------------------------
# type() : returns the type of a datatype
# ---------------------------------------------------------
print (type(10)) # int => Integer
print (type(10.3)) # float => Floating Point Number
print (type("Kareem Ashraf")) # str => String
print (type(2+4j)) # complex => Complex Number
print (type(2 == 2)) # bool => Boolean
print (2 == 3) # it prints : False
print (2 == 2) # it prints : True
print ([1, 2, 3, 4, 5]) # list holding these values
print (type([1, 2, 3, 4, 5])) # list => List just like arrays in C++
print ((1, 2, 3, 4, 5)) # tuple holding these values
print (type((1, 2, 3, 4, 5))) # tuple => Tuple
print ({"one " : 1, "two " : 2, "three " : 3})
print (type({"one " : 1, "two " : 2, "three " : 3})) #dict => Dictionary
